module Itefu::Color
  
  # 定数色をキャッシュする
  module Const
    @@const_color = {}    # [Hash<Array, Color>] 定数色のキャッシュ
    def self.new(*args)
      # Rgss3のColorはfreezeしても変更できてしまうが, 変更しないでくれ, という気持ちだけはこめてfreezeする
      @@const_color[args] ||= ::Color.new(*args).freeze
    end
  end
  
  # 定数色の宣言
  module Declaration
    # 色名を定義する
    def declare_color(name, r, g, b, a = 0xff)
      Itefu::Utility::Module.declare_lazy_constant(self, name, Itefu::Color::Const, r, g, b, a)
    end
  end
  extend Itefu::Color
  extend Declaration

  # @return [Color] 定数色を取得する
  # @note 初回のみColorインスタンスを生成する
  def self.const(r, g, b, a = 0xff)
    Itefu::Color::Const.new(r, g, b, a)
  end 
  
  # @return [Color] 色のインスタンスを生成する
  def self.create(r, g, b, a = 0xff)
    ::Color.new(r, g, b, a)
  end

